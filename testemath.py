import math
x=float(input("Digite um número: "))

print("O valor absoluto de ", x, "é", math.fabs(x))
print("O maior número inteiro menor ou igual a ", x, "é", math.floor(x))
print("O menor número inteiro maior ou igual a ", x, "é", math.ceil(x))
print("A raiz quadrada de ", x, "é", math.sqrt(x))
print("A parte inteira de ", x, "é", math.trunc(x))
print("O fatorial de ", x, "é", math.factorial (x))
